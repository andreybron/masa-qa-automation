package Lesson_2;

public class Variablels {
    public static void main(String[] args) {
        byte myByte;

        myByte = 4;

        byte myByte2 = -127;

        short myShort = 2345;

        int myInt = 123456789;

        long myLong = 123242424243242423L;

        float myFloat = 4.69f;

        double myDouble =99.99;

        boolean isTrue = true;
        boolean isFalse = false;

        char myChar = '\u2607';
        char myChar2 = 'B';

        String myName = "Andrey";

        System.out.println("myByte=" + myByte);
        System.out.println(myByte2);
        System.out.println("myByte2 = " + myByte2);
        System.out.println(myShort);
        System.out.println(myInt);
        System.out.println(myLong);
        System.out.println("myFloat = " + myFloat); // myFloat.soutv
        System.out.println("myDouble = " + myDouble);
        System.out.println("isTrue = " + isTrue);
        System.out.println("isFalse = " + isFalse);
        System.out.println("myChar = " + myChar);
        System.out.println("myChar2 = " + myChar2);
        System.out.println("myName = " + myName);
    }
}
